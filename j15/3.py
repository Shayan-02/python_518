def sumNumbers (num1: float, num2: float) -> float:
    """
    Get two number and return sum of them.
    
    Args:
        num1 (float) : first number
        num2 (float) : second number
    
    Returns:
        y (float) : sum of num1 and num2
    """
    y = num1 + num2
    return y

